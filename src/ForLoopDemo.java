public class ForLoopDemo {
    public static void main(String[] args) {
        for (int i = 1; i < 6; i++)
            //Start with 1 AND EXCUTE WHATS INSIDE THE FOR LOOP
            // adding the number until it reaches 6
            System.out.println("Ketchup is better than Mustard");
        //printed 5 times.

    }
}
